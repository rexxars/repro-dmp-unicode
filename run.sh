#!/bin/sh

echo '=== [ Dart ] =================================================================='
(cd dart && dart main.dart)

echo '=== [ Java ] =================================================================='
(cd java && rtx x java -- gradle run -q)

echo '=== [ JavaScript ] ============================================================'
(cd js && node main.js)

echo '=== [ Python ] ================================================================'
(cd python && python main.py)

echo '=== [ Rust ] =================================================================='
(cd rust && cargo run -q)

echo '=== [ Go ] ===================================================================='
(cd go && go run main.go)
